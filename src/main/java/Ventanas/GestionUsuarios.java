    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
     try{
          int fila = tblUsuarios.getSelectedRow();
    if (fila == -1) {
      JOptionPane.showMessageDialog(this,"fila no seleccionada,seleccione fila para eliminar");
    }

    int id = (int) tblUsuarios.getValueAt(fila, 0);

    cliente.eliminarUsuario(id);
    JOptionPane.showMessageDialog(this, "usuario eliminado.");
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String texto=txtID.getText();
        if(!texto.isEmpty()){
            try{
               int id=Integer.parseInt(texto);
               Usuario usuario=cliente.buscarUsuarioPorId(id);
               DefaultTableModel model1=new DefaultTableModel();
               model1.setColumnIdentifiers(new String[]{"Id","nombre","email","contraseña"});
               
               if(usuario!=null){
               model1.addRow(new Object[]{usuario.getId(),usuario.getNombre(),usuario.getCorreoElectronico(),usuario.getContrasena()});
               
               
               }else{
                   JOptionPane.showMessageDialog(this,"usuario no encontrado");
               }
               tblUsuarios.setModel(model1);
            }catch(IOException e){
                JOptionPane.showMessageDialog(this, e);
            }
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

