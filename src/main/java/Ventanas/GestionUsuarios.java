    public void cargarUsuarios(){
        try{
            List<Usuario> usuarios = cliente.listarTodosUsuarios();
            DefaultTableModel model=new DefaultTableModel();
            model.setColumnIdentifiers(new Object[]{"Id","nombre","email","contraseña"});
            for(Usuario u: usuarios){
                model.addRow(new Object[]{u.getId(),u.getNombre(),u.getCorreoElectronico(),u.getContrasena()});
            }
            tblUsuarios.setModel(model);
            tblUsuarios.revalidate();
            tblUsuarios.repaint();
        }catch(IOException e){
            JOptionPane.showMessageDialog(this,"error al cargar usuarios");
        }
    }
